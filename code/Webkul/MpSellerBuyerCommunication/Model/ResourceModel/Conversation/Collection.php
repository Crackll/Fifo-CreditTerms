namespace Webkul\MpSellerBuyerCommunication\Model\ResourceModel\Conversation;

use \Webkul\MpSellerBuyerCommunication\Model\ResourceModel\AbstractCollection;

/**
 * Webkul MpSellerBuyerCommunication ResourceModel Conversation collection
 */
class Collection extends AbstractCollection
{
    /**
     * @var string
     */
    protected $_idFieldName = 'entity_id';

    /**
     * Define resource model
     *
     * @return void
     */
    protected function _construct()
    {
        $this->_init(
            \Webkul\MpSellerBuyerCommunication\Model\Conversation::class,
            \Webkul\MpSellerBuyerCommunication\Model\ResourceModel\Conversation::class
        );
        $this->_map['fields']['entity_id'] = 'main_table.entity_id';
    }

    /**
     * Add filter by store
     *
     * @param int|array|\Magento\Store\Model\Store $store
     * @param bool $withAdmin
     * @return $this
     */
    public function addStoreFilter($store, $withAdmin = true)
    {
        if (!$this->getFlag('store_filter_added')) {
            $this->performAddStoreFilter($store, $withAdmin);
        }
        return $this;
    }
}
