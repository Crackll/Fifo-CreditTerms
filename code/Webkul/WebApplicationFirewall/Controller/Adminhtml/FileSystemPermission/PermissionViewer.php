namespace Webkul\WebApplicationFirewall\Controller\Adminhtml\FileSystemPermission;

class PermissionViewer extends \Magento\Backend\App\Action
{
    /**
     * Authorization level of a basic admin session
     * @see _isAllowed()
     */
    const ADMIN_RESOURCE = 'Webkul_WebApplicationFirewall::permissionviewer';

    /**
     * @var $resultPageFactory
     */
    protected $resultPageFactory;

    /**
     * Constructor
     * @param \Magento\Backend\App\Action\Context  $context
     * @param \Magento\Framework\View\Result\PageFactory $resultPageFactory
     */
    public function __construct(
        \Magento\Backend\App\Action\Context $context,
        \Magento\Framework\View\Result\PageFactory $resultPageFactory
    ) {
        $this->resultPageFactory = $resultPageFactory;
        parent::__construct($context);
    }

    /**
     * Execute view action
     * @return \Magento\Framework\Controller\ResultInterface
     */
    public function execute()
    {
        $resultPage = $this->resultPageFactory->create();
        $resultPage->setActiveMenu('Webkul_WebApplicationFirewall::permissionviewer');
        $resultPage->getConfig()->getTitle()->prepend(__('View Permissions'));
        return $resultPage;
    }
}
