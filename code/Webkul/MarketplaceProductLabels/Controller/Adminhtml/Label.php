namespace Webkul\MarketplaceProductLabels\Controller\Adminhtml;

use Magento\Backend\App\Action;

abstract class Label extends \Magento\Backend\App\Action
{
    /**
     * Check for is allowed
     *
     * @return boolean
     */
    protected function _isAllowed()
    {
        return $this->_authorization->isAllowed('Webkul_MarketplaceProductLabels::seller_label_grid');
    }
}
